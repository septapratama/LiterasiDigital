import 'package:flutter/material.dart';

class customText {
  static TextStyle Poppins_SemiBold(double FontSize,Color color, FontWeight fontWeight) {
    return TextStyle(
      fontFamily: 'Poppins_SemiBold',
      color: color,
      fontSize: FontSize,
      fontWeight: fontWeight
    );
  }
  static TextStyle PoppinsBold(double FontSize,Color color,FontWeight fontWeight){
    return TextStyle(
      fontFamily: 'Poppins_Bold',
      color: color,
      fontSize: FontSize,
      fontWeight: fontWeight
    );
  }
}






